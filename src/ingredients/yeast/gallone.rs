/// If the yeast has the STA1 gene, allowing it to secrete
/// glucoamylase, an enzyme that breaks down complex
/// carbohydrates (dextrins).  Makes very dry beers, but also
/// can be a cause of excess fermentation in secondary causing
/// bottle bombs.
#[derive(Debug, PartialEq, Eq, Hash, Clone, Copy, Serialize, Deserialize)]
pub enum STA1 {
    /// Does not express STA1 gene
    None,

    /// Partial match for STA1 gene (might be defective)
    Partial,

    /// Full match of STA1 gene
    Full,
}
